function ShadowPhantomBuffCalc(Class, Level)
	local IncDamage = 0
	local IncDefense = 0
	local BuffTime = (Level / 6 + 30) * 60
	
	if (Level <= 180) then
		IncDamage = Level / 3 + 45
		IncDefense = Level / 5 + 50
	else
		IncDamage = 105
		IncDefense = 86
	end
	
	return IncDamage, IncDefense, BuffTime
end